def get_location_id(city: str) -> str:
    """根据城市名称获取和风天气 location_id。

    支持: 城市名、城市名+市、常见景区名。
    找不到时返回空字符串。
    """
    if not city:
        return ""

    city = city.strip()

    # 精确匹配
    if city in CITY_CODES:
        return CITY_CODES[city]

    # 去除 "市" 后缀再试
    if city.endswith("市") and city[:-1] in CITY_CODES:
        return CITY_CODES[city[:-1]]

    # 部分匹配：遍历所有 key，找包含 city 或 city 包含 key 的
    for name, code in CITY_CODES.items():
        if city in name or name in city:
            return code

    return ""


