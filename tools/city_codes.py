def get_city_info(city: str) -> dict | None:
    """获取城市的旅游信息(景点+交通提示)。"""
    if not city:
        return None

    city = city.strip()
    if city in CITY_INFO:
        return CITY_INFO[city]
    if city.endswith("市") and city[:-1] in CITY_INFO:
        return CITY_INFO[city[:-1]]

    for name, info in CITY_INFO.items():
        if city in name or name in city:
            return info

    return None
